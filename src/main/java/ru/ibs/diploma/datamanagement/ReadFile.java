package ru.ibs.diploma.datamanagement;

import java.io.IOException;
import java.util.List;

/**
 * Универсальный интерфейс для чтения файлов и преобразования их содержимого
 * в коллекцию объектов заданного типа.
 *
 * <p>Реализации могут работать с различными источниками (CSV, JSON, XML и т.д.)
 * и использовать любой механизм маппинга (Jackson, OpenCSV, собственный парсер и др.).
 *
 * <p>Метод является generic-утилитой: возвращает список объектов именно того типа,
 * который передан в качестве параметра {@code type}, обеспечивая compile-time
 * безопасность приведения типов.
 *
 */
public interface ReadFile {
    /**
     * Читает файл и преобразует его содержимое в список объектов указанного класса.
     *
     * @param type класс объектов, в которые требуется преобразовать строки файла
     * @return список заполненных объектов; никогда не {@code null}
     * @throws IOException если чтение или парсинг файла невозможны
     *                     (файл не найден, повреждён, нет прав доступа и т.д.)
     */
    public <T> List<T> readFile(Class<T> type) throws IOException;
}
